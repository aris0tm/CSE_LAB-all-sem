# integer
integer_number = 42
print("Integer Number:", integer_number)
print("Type:", type(integer_number))

# Float
float_number = 3.14
print("\nFloat Number:", float_number)
print("Type:", type(float_number))

# Complex
complex_number = 2 + 3j
print("\nComplex Number:", complex_number)
print("Type:", type(complex_number))

# Boolean
boolean = True
print("\nBoolean:", boolean)
print("Type:", type(boolean))

# String
string = "Hello, World!"
print("\nString:", string)
print("Type:", type(string))

# List
number_list = [1, 2, 3, 4, 5]
print("\nList of Numbers:", number_list)
print("Type:", type(number_list))

# Tuple
number_tuple = (6, 7, 8, 9, 10)
print("\nTuple of Numbers:", number_tuple)
print("Type:", type(number_tuple))

# Dictionary
number_dict = {'a': 1, 'b': 2, 'c': 3}
print("\nDictionary of Numbers:", number_dict)
print("Type:", type(number_dict))

# Set
number_set = {1, 2, 3, 4, 5}
print("\nSet of Numbers:", number_set)
print("Type:", type(number_set))

# None
none_value = None
print("\nNone Value:", none_value)
print("Type:", type(none_value))
